package br.com.fag;

public class TipoPagamento {
    String credito;
    String debito;
    String pix;
    String avista;

    TipoPagamento(String credito, String debito, String pix, String avista) {
        credito = credito;
        debito = debito;
        pix = pix;
        avista = avista;
    }

    public String getCredito() {
        return credito;
    }

    public String getDebito() {
        return credito;
    }

    public String getPix() {
        return credito;
    }

    public String getAvista() {
        return credito;
    }

}
